class VariablesTipos{
	public static void main(String[] args) {
		int numEntero = 23;
		double numDecimal = 23.69;
		boolean verdadero= true;
		char caracter = 'C';
		String cadena= "Nayla";
	
	System.out.println("Valor de Int:" + numEntero);	
	System.out.println("Valor de double:" + numDecimal);
	System.out.println("Valor de boolean:" + verdadero);
	System.out.println("Valor de caracter:" + caracter);
	System.out.println("Valor de string:" + cadena);
	}
}